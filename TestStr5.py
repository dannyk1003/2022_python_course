print('Str: %s, Int: %d, Float: %f' %('Hello', 10, 3.14)) # %s = str, %d = int, %f = float

print('Str: %s, Int: %d, Float: %.2f' %('Hello', 10, 3.14))
print('Str: %s, Int: %5d, Float: %f' %('Hello', 10, 3.14))
print('Str: %3s, Int: %d, Float: %5.2f' %('Hello', 10, 3.14))

print('Str: %7s, Int: %d, Float: %5.2f' %('Hello', 10, 3.14))
print('Str: %-7s, Int: %d, Float: %5.2f' %('Hello', 10, 3.14))